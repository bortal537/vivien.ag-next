* Added keyboard events for modals.
    * __ESC__ will dismiss the modal (if no changes has been done).
    * __ESC__ will toggle the confirmation modal (if changes has been done).
        * __Enter__ will dismiss the modal (if it's in confirmation state).
* Changed the orientation of the modal window to "align-bottom" for mobile devices.
* Changed the orientation of the modal actions to "justify-end" for mobile devices.
* Changed the sequence of the modal actions for mobile devices.
* Fix price position for mobile devices.
